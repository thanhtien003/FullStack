// 1. JSON
//    (a) <JSON> là 1 định dạng dữ liệu (Chuỗi)
//    (b) <JSON> là:(JavaScript Object Notation)
//    (c)<JSON> thể hiện được dạng dữ liệu trong JavaScript (Number, Boolean, String, Object,Null,Array)
//    (d) Mã hóa(Encode) / Giải mã(Decode)
//    (e) Stringify (Chuyển sang dạng JSON) / Parse(Chuyển sang dạng JavaScript)
// <<<<< Parse(Chuyển sang dạng JavaScript)>>>>>>
var a = '1'
var json = '["JavaScript","PHP"]';
var json1 = '{"name": "JavaScript","age": 18}';
var num = JSON.parse(a);
var arr = JSON.parse(json);
var object = JSON.parse(json1);
console.log(num);
console.log(arr);
console.log(object);

// <<<<< Parse(Chuyển sang dạng JSON)>>>>>>
console.log(JSON.stringify(1));
console.log(JSON.stringify(true));
console.log(JSON.stringify(null));
console.log(JSON.stringify(["JavaScript", "PHP"]));
console.log(JSON.stringify({ "name": "JavaScript", "age": 18 }));


/* Giới thiệu về toán tử trong JavaScript */

    // 1. Toán tử số học - Arithmetic
        var a=1+2;
        console.log(a);

    // 2. Toán tử gán - Asignment
        var fullName="Pham Thanh Tien"
        console.log(fullName);

    // 3. Toán tử so sánh - Comparison
        var b=1
        var c=2
        if(b>c){
            console.log("b>c")
        }else{
            console.log("b<=c")
        }
        
    // 4. Toán tử logic - Logical
        var d =1;
        var e=2;
        if(d>0&&d<e){
            console.log("d>0&&d<e")
        }
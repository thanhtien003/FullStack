//* Toán Tử Gán */
// Toán Tử     Ví dụ        Tương đương
// =            x   = y       x = y
var a = 1; // 1 gán cho a

// +=           x   += y       x = x + y
a += 2; // a + 2 gán cho a

// -=           x   -= y       x = x - y
a -= 2; // a - 2 gán cho a

// *=           x   *= y       x = x * y
a *= 2; // a * 2 gán cho a

// /=           x   /= y       x = x / y
a /= 2; // a / 2 gán cho a

// **=          x   **= y       x = x ** y
a **= 2; // a ** 2 gán cho a

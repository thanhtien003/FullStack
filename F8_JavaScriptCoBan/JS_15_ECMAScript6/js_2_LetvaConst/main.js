// 1. Let, Const
//      ++ Var / Let, Const: Khác nhau: Scope(Phạm vi truy cập), Hosting (Đưa lên trên đầu),
//      ++ Const / Var ,Let: Khác nhau: Assignment(Gán lại giá trị cho những biến).const không gán được giá trị ở lần 2
//      ++ Code block: if else, loop, {},...
//      ++ Code thuần : Var
//      ++ Bable : Const , let
//      ++ Const : Khi định nghĩa biến và không gán lại biến đó dùng Const
//      ++ Let : Khi cần gán lại giá trị cho biến dùng Let
{
    var code = "Javascript"
}
console.log(code);

{
    let code1 = "Javascript"
}
console.log(code1);
{
    const code2 = "Javascript"
}
console.log(code2);

